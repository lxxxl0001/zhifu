package com.hello.zhifu.model;

public class Flowing {
	
	private Long flowid;
	private Integer userid;
	private Long termNum;
	private Integer carNum;
	private Integer buyAmount;
	private Integer haveAmount;
	private Integer isPay;
	private Integer isOpen;
	private Integer isSend;
	
	public Long getFlowid() {
		return flowid;
	}
	public void setFlowid(Long flowid) {
		this.flowid = flowid;
	}
	public Integer getUserid() {
		return userid;
	}
	public void setUserid(Integer userid) {
		this.userid = userid;
	}
	public Long getTermNum() {
		return termNum;
	}
	public void setTermNum(Long termNum) {
		this.termNum = termNum;
	}
	public Integer getCarNum() {
		return carNum;
	}
	public void setCarNum(Integer carNum) {
		this.carNum = carNum;
	}
	public Integer getBuyAmount() {
		return buyAmount;
	}
	public void setBuyAmount(Integer buyAmount) {
		this.buyAmount = buyAmount;
	}
	public Integer getHaveAmount() {
		return haveAmount;
	}
	public void setHaveAmount(Integer haveAmount) {
		this.haveAmount = haveAmount;
	}
	public Integer getIsPay() {
		return isPay;
	}
	public void setIsPay(Integer isPay) {
		this.isPay = isPay;
	}
	public Integer getIsOpen() {
		return isOpen;
	}
	public void setIsOpen(Integer isOpen) {
		this.isOpen = isOpen;
	}
	public Integer getIsSend() {
		return isSend;
	}
	public void setIsSend(Integer isSend) {
		this.isSend = isSend;
	}
}
